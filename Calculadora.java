package aplicacao;

public class Calculadora {
	
	
	/* Classe que tem apenas instruções para executar */
	
    public int somaDeValores(int numeroA, int numeroB){
        return numeroA + numeroB;
    }

    public int subtracaoDeValores(int numeroA, int numeroB){
        return numeroA - numeroB;
    }

    public int multiplicacaoDeValores(int numeroA, int numeroB){
        return numeroA * numeroB;
    }

    public int divisaoDeValores(int numeroA, int numeroB){
        return numeroA / numeroB;
    }
	
    


}
